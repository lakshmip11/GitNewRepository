
public class Timing {

	public static void main(String[] args) {
		int Time =4;
		 if (Time>=0 && Time <8) {
			System.out.println("Day-Morning");
		}
		else if (Time>=8 && Time>16) {
			System.out.println("Afternoon");
		}
		else if (Time>=16 && Time>24) {
			System.out.println("Evening");
		}
		}
	}


